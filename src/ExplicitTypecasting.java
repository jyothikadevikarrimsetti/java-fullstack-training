public class ExplicitTypecasting {
    public static void main(String args[]){
        double i = 2832.2323;
        short j =(short)i;
        long k =(long)i;
        System.out.println("before type casting"+i);
        System.out.println("after type casting to short"+j);
        System.out.println("after type casting to long"+k);
    }
}

public class EvenArray {
    public static void main(String[] args) {
        int[] arr = {22,13,45,65,80};
        for(int i=0; i<arr.length; i++){
            if(arr[i]%2 ==0){
                System.out.println(arr[i]);
            }
            else{
                System.out.println(arr[i]);
            }
        }
    }
}

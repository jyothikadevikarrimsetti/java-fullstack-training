public class Conditionalelseif {
    public static void main(String args[]){
        int x =20;
        int y =30;
        if(x>20){
            System.out.println("condition is true");
        }
        else if(x<y){
            System.out.println("Correct condition");
        }
        else{
            System.out.println("condition is false");
        }
    }
}

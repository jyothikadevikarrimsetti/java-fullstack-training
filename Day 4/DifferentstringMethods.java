public class DifferentstringMethods {
    public static void main(String args[]){
        String quote = "love it or leave it";
        System.out.println("length of the string is:"+quote.length());
        System.out.println("the upper case of the string is:"+quote.toUpperCase());
        System.out.println("the lower case of the string is:"+quote.toLowerCase());
        System.out.println(quote.indexOf("love" ));
    }
}
